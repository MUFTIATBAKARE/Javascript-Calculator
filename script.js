var opr = prompt("operator: ")
var number1 = parseInt(prompt("Number 1: "))
var number2 = parseInt(prompt("Number 2: "))

if(opr == "+")
{
    console.log(number1 + number2)
}
else if(opr == "-")
{
    console.log(number1 - number2)
}
else if(opr == "*")
{
    console.log(number1 * number2)
}
else if(opr == "/")
{
    console.log(number1 / number2)
}